import { readdir, readFile, stat } from 'node:fs/promises';
import path from 'node:path';

export async function buildGallery(root) {
  const builtIns = await readBuiltInCards(root);
  const benchmarks = await readBenchmarkCards(root);
  const sections = [
    `## Built-in levels\n\n${builtIns.join('\n\n---\n\n')}`,
  ];
  if (benchmarks.length > 0) sections.push(`## Benchmark levels\n\n${benchmarks.join('\n\n---\n\n')}`);
  const header = '# Level gallery\n\nGenerated by `npm run gallery`. Edit `src/levels/<id>/level.md` for built-in content or `src/benchmark-levels/<id>/level.md` for benchmark content, then re-run `npm run gallery`.\n';
  return `${header}\n${sections.join('\n\n')}\n`;
}

async function readBuiltInCards(root) {
  const registryPath = path.join(root, 'src', 'levels', 'index.ts');
  const registry = await readFile(registryPath, 'utf8');
  const order = parseRegistryOrder(registry);
  const imports = parseSwitchImports(registry);
  const cards = [];
  for (const { id } of order) {
    const modulePath = imports.get(id) ?? `./${id}`;
    const dir = modulePath.replace(/^\.\//, '');
    cards.push(await readCard(path.join(root, 'src', 'levels', dir, 'level.md'), `src/levels/${dir}/level.md`));
  }
  return cards;
}

async function readBenchmarkCards(root) {
  const benchmarkRoot = path.join(root, 'src', 'benchmark-levels');
  let children;
  try {
    children = await readdir(benchmarkRoot, { withFileTypes: true });
  } catch (error) {
    if (error?.code === 'ENOENT') return [];
    throw error;
  }
  const cards = [];
  for (const child of children.filter((entry) => entry.isDirectory()).sort((a, b) => a.name.localeCompare(b.name))) {
    const directory = child.name;
    const directoryRoot = path.join(benchmarkRoot, directory);
    const descriptorPath = path.join(directoryRoot, 'level.json');
    const modulePath = path.join(directoryRoot, 'index.ts');
    const descriptor = JSON.parse(await readFile(descriptorPath, 'utf8'));
    if (descriptor.id !== directory) throw new Error(`Benchmark descriptor ${relative(root, descriptorPath)} id must match directory ${directory}.`);
    if (typeof descriptor.title !== 'string' || descriptor.title.length === 0) throw new Error(`Benchmark descriptor ${relative(root, descriptorPath)} must contain a title.`);
    await stat(modulePath);
    const card = await readCard(path.join(directoryRoot, 'level.md'), `src/benchmark-levels/${directory}/level.md`);
    if (!card.startsWith(`# ${descriptor.title}`)) throw new Error(`Benchmark card ${relative(root, path.join(directoryRoot, 'level.md'))} must start with # ${descriptor.title}.`);
    cards.push(card);
  }
  return cards;
}

async function readCard(cardPath, displayPath) {
  try {
    return (await readFile(cardPath, 'utf8')).trimEnd();
  } catch (error) {
    if (error?.code === 'ENOENT') throw new Error(`Missing identity card for ${displayPath}`);
    throw error;
  }
}

function parseRegistryOrder(source) {
  const arrayMatch = source.match(/export const levelMetadatas: (?:LevelMetadata|BuiltInLevelMetadata)\[\] = \[([\s\S]*?)\n\];/);
  if (!arrayMatch) throw new Error('Could not find levelMetadatas array');
  const entries = [];
  const entryRegex = /\{\s*id:\s*'([^']+)'\s*,\s*title:\s*'([^']+)'/g;
  let match;
  while ((match = entryRegex.exec(arrayMatch[1]))) entries.push({ id: match[1], title: match[2] });
  return entries;
}

function parseSwitchImports(source) {
  const map = new Map();
  // Built-in loaders are part of the human-maintained registry. Benchmark
  // loaders are deliberately not parsed here; their directories are scanned.
  const loaderRegex = /'([^']+)': async \(\) => \(await import\('([^']+)'\)\)/g;
  let match;
  while ((match = loaderRegex.exec(source))) map.set(match[1], match[2]);
  return map;
}

function relative(root, filePath) {
  return path.relative(root, filePath).replaceAll(path.sep, '/');
}
